class NutritionData {
  double proteins;
  double carbs;
  double fat;
  double sugar;
  double fiber;


  NutritionData({double proteins, double carbs, double fat, double sugar, double fiber})
      : this.proteins = proteins,
        this.carbs = carbs,
        this.fat = fat,
        this.sugar = sugar,
        this.fiber = fiber;
}